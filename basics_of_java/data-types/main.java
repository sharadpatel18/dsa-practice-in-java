public class main {
    public static void main(String[] args){
        int x = 10;
        System.out.println(x);

        float x_float = 10.5f;
        System.out.println(x_float);

        char x_char = 'a';
        System.out.println(x_char);

        boolean x_bool = true;
        System.out.println(x_bool);

        String x_string = "Hello World!";
        System.out.println(x_string);

        long x_long = 10L;
        System.out.println(x_long);

        double x_double = 10.5;
        System.out.println(x_double);

        byte x_byte = 10;
        System.out.println(x_byte);

        short x_short = 10;
        System.out.println(x_short);

    }
}